### 💡 [문제 6] 전문가 해설: PRIMARY KEY(기본키)의 이해

6번 문제의 시나리오를 실행하면 **두 번째 INSERT 문에서 에러가 발생**하여 데이터가 들어가지 않습니다. 그 이유는 다음과 같습니다.

#### 1\. 어떤 에러가 발생하는가?

데이터베이스 시스템마다 메시지는 조금씩 다르지만, 핵심은 **"중복 키(Duplicate Entry)"** 또는 **"고유 제약 조건 위반(Unique Constraint Violation)"** 에러입니다.

> *예시 (MySQL):* `Error Code: 1062. Duplicate entry '1' for key 'books.PRIMARY'`

#### 2\. 왜 발생하는가?

첫 번째 쿼리에서 `book_id`가 **1**인 데이터를 이미 저장했습니다. 그런데 두 번째 쿼리에서 또다시 `book_id`를 **1**로 설정하여 입력을 시도했기 때문입니다.
`book_id` 컬럼은 `PRIMARY KEY`로 설정되어 있으므로, **이미 존재하는 식별자(ID)를 다시 사용할 수 없습니다.**

#### 3\. PRIMARY KEY (기본키)의 핵심 규칙

전문가로서 PK(Primary Key)를 정의할 때 항상 강조하는 두 가지 불변의 규칙이 있습니다.

  * **유일성 (Uniqueness):** 테이블 내의 모든 행(Row)은 PK 컬럼에서 **서로 다른 값**을 가져야 합니다. 중복된 값은 절대 허용되지 않습니다. (주민등록번호가 전 국민마다 다른 것과 같습니다.)
  * **NOT NULL (최소성):** PK 컬럼은 \*\*비어있는 값(NULL)\*\*을 가질 수 없습니다. 식별자는 반드시 값이 존재해야 합니다.
